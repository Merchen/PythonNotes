# 元组内部元素不可修改，但元素变量可修改（变量可随意赋值）
# 元组内所有元素可散列时，元组才可散列

dem1 = (200, 50)
# (200, 50)
a = dem1[0]
# 200

eval('(200, 50) + (200,)')
# (200, 50, 200)

eval('(200, 100) + (100, 50)')
# (200, 100, 100, 50)

# 拆包
a1, b1 = (100, 200)
# a = 100, b = 200
a2, b2, *c = range(5)
# 0, 1, [2, 3, 4]

# 元祖中含可变对象
tup = (1, 2, [3, 4])
try:
    tup[2] += [4,5]
    # (1, 2, [3, 4, 4, 5])
except TypeError:
    pass