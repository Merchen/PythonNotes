###  集合
########################################################################
# 不支持索引且自动去重， 元素以hash表形式存储
# 最好不要在迭代集合或字典时添加值，考虑到存储字典元素的无序性（python3.6之后有序）

set(range(5))
# {0, 1, 2, 3, 4}

set1 = {1, 4, 4, 3} # 构建集合速度最快
# {1, 3, 4}

set1.add(5)
# {1, 3, 4, 5}
set1.pop()
# {3, 4, 5}
set1.remove(5)
# {3, 4}

set2 = {1, 2}.union({2, 3})
# {1, 2, 3}
set3 = {1, 2}.union([2, 3])
# {1, 2, 3}

set4 = {1, 2} | {2 ,3}
# {1, 2, 3}

set5 = {1, 2} & {2, 3}
# {2}

set6 = {1, 2} ^ {2, 3}
# {1, 3}

set7 = {1, 2} - {2, 3}
# {1}

set8 = {i for i in range(3)}
# {0, 1, 2}

set9 = {1, 2, 3} > {1, 2}
# True